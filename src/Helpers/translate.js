export const translate = (word) => {
    if(word==="intelligence") return "Inteligencia";
    if(word==="strength") return "Fuerza";
    if(word==="speed") return "Velocidad";
    if(word==="durability") return "Durabilidad";
    if(word==="power") return "Poder";
    if(word==="combat") return "Combate";
}